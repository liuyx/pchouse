package cn.com.pcgroup.android.model;

public class BookShelf {
	private String id;
	private String magazine; //杂志类型
	private String publisher; //发行者
	private String volume; //卷
	private String bookDownloadStatus; //订阅状态
	private String issue; //几月刊
	private String publishTime; //发行时间
	private String cover; //封面大图
	private String thumb; //缩略图
	private String dir_hor;
	private String dir_ver;
	private String summary; //导引
	private String url; //杂志包
	private String deviceType; //设备类型
	private String md5;
	private String bestTopicUrl;
	private String size; //杂志包大小
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMagazine() {
		return magazine;
	}
	public void setMagazine(String magazine) {
		this.magazine = magazine;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getVolume() {
		return volume;
	}
	public void setVolume(String volume) {
		this.volume = volume;
	}
	public String getBookDownloadStatus() {
		return bookDownloadStatus;
	}
	public void setBookDownloadStatus(String bookDownloadStatus) {
		this.bookDownloadStatus = bookDownloadStatus;
	}
	public String getIssue() {
		return issue;
	}
	public void setIssue(String issue) {
		this.issue = issue;
	}
	public String getPublishTime() {
		return publishTime;
	}
	public void setPublishTime(String publishTime) {
		this.publishTime = publishTime;
	}
	public String getCover() {
		return cover;
	}
	public void setCover(String cover) {
		this.cover = cover;
	}
	public String getThumb() {
		return thumb;
	}
	public void setThumb(String thumb) {
		this.thumb = thumb;
	}
	public String getDir_hor() {
		return dir_hor;
	}
	public void setDir_hor(String dir_hor) {
		this.dir_hor = dir_hor;
	}
	public String getDir_ver() {
		return dir_ver;
	}
	public void setDir_ver(String dir_ver) {
		this.dir_ver = dir_ver;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getMd5() {
		return md5;
	}
	public void setMd5(String md5) {
		this.md5 = md5;
	}
	public String getBestTopicUrl() {
		return bestTopicUrl;
	}
	public void setBestTopicUrl(String bestTopicUrl) {
		this.bestTopicUrl = bestTopicUrl;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	
}
